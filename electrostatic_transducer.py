import numpy as np

# ─────────────────────────────────────────────
# CONSTANTS
# ─────────────────────────────────────────────

HBAR      = 1.0545718e-34       # reduced Planck constant (J·s)
K_B       = 1.380649e-23        # Boltzmann constant (J/K)
EPSILON_0 = 8.8541878128e-12    # permittivity of free space (F/m)
E_CHARGE  = 1.602176634e-19     # elementary charge (C)
MU_0      = 4 * np.pi * 1e-7   # permeability of free space (T·m/A)
GAMMA     = 1.7608597e11        # gyromagnetic ratio (rad/s/T)

# ─────────────────────────────────────────────
# SILICON PROPERTIES
# ─────────────────────────────────────────────

SILICON = {
    "epsilon_r":            11.7,       # relative permittivity
    "dielectric_strength":  30e6,       # V/m (breakdown field)
    "resistivity_intrinsic": 2300,      # Ohm·m at 300K
    "resistivity_doped":    0.01,       # Ohm·m (moderately doped)
    "density":              2329,       # kg/m^3
    "youngs_modulus":       170e9,      # Pa
    "poisson_ratio":        0.28,
    "c_sound_long":         8433,       # m/s longitudinal [100]
    "c_sound_shear":        5843,       # m/s shear [100]
    "thermal_conductivity": 148,        # W/(m·K) at 300K
    "thermal_expansion":    2.6e-6,     # 1/K
    "debye_temp":           645,        # K
    "bandgap_eV":           1.12,       # eV at 300K
    "electron_mobility":    0.145,      # m^2/(V·s)
    "hole_mobility":        0.045,      # m^2/(V·s)
    "oxide_thickness_nm":   2,          # native oxide SiO2
}

# Quartz piezo source (AT-cut resonator)
QUARTZ_PIEZO = {
    "d_26":       3.1e-12,      # piezo strain constant (C/N)
    "epsilon_r":  4.5,          # relative permittivity
    "k_sq":       0.0081,       # electromechanical coupling k^2
    "Q_mech":     1e6,          # mechanical Q at 300K
    "c_shear":    3764.0,       # shear wave speed (m/s)
    "rho":        2650.0,       # density (kg/m^3)
}


# ─────────────────────────────────────────────
# PIEZOELECTRIC VOLTAGE SOURCE
# ─────────────────────────────────────────────

def piezo_voltage_from_strain(strain, thickness_m, d_coeff=3.1e-12,
                               epsilon_r=4.5, Q_mech=1e6):
    """
    Voltage generated by piezoelectric quartz under mechanical strain.

    In open-circuit condition:
      V = d * stress * thickness / epsilon
      stress = strain * rho * c^2 (elastic)
      At resonance: strain_resonant = strain_static * Q

    strain      : mechanical strain (dimensionless)
    thickness_m : crystal thickness (m)
    d_coeff     : piezoelectric coefficient (C/N)
    epsilon_r   : relative permittivity
    Q_mech      : mechanical quality factor (amplification at resonance)
    returns     : dict with voltages and fields
    """
    rho = QUARTZ_PIEZO["rho"]
    c = QUARTZ_PIEZO["c_shear"]

    stress = strain * rho * c**2  # Pa
    # Piezo charge: q = d * stress * area
    # Open-circuit voltage: V = d * stress * t / (epsilon_0 * epsilon_r)
    V_static = d_coeff * stress * thickness_m / (EPSILON_0 * epsilon_r)
    V_resonant = V_static * Q_mech

    E_field_static = V_static / thickness_m
    E_field_resonant = V_resonant / thickness_m

    # Power available (at matched impedance)
    # P = V^2 / (4R), R = 1/(omega * C), C = epsilon * A / t
    f_res = QUARTZ_PIEZO["c_shear"] / (2 * thickness_m)
    omega = 2 * np.pi * f_res
    # For a 5mm diameter crystal
    A = np.pi * (2.5e-3)**2
    C_crystal = EPSILON_0 * epsilon_r * A / thickness_m
    Z_crystal = 1 / (omega * C_crystal) if omega > 0 else np.inf
    P_available = V_resonant**2 / (4 * Z_crystal) if Z_crystal > 0 else 0

    return {
        "V_static_V": V_static,
        "V_resonant_V": V_resonant,
        "E_field_static_Vm": E_field_static,
        "E_field_resonant_Vm": E_field_resonant,
        "stress_Pa": stress,
        "f_resonance_Hz": f_res,
        "C_crystal_F": C_crystal,
        "Z_crystal_ohm": Z_crystal,
        "P_available_W": P_available,
    }


def piezo_voltage_from_magnon(delta_B_T, fe_ppm=100, eta_cm=1.5,
                               thickness_m=0.1e-3, Q_mech=1e6):
    """
    Full signal chain: geomagnetic field variation -> piezo voltage.

    delta_B_T   : magnetic field perturbation (T)
    fe_ppm      : iron defect concentration in quartz
    eta_cm      : spin-phonon coupling strength (cm^-1)
    thickness_m : crystal thickness (m)
    Q_mech      : mechanical Q factor
    returns     : dict with full chain analysis
    """
    # Magnon frequency shift from field variation
    delta_omega_magnon = GAMMA * MU_0 * delta_B_T
    delta_f_magnon = delta_omega_magnon / (2 * np.pi)

    # Phonon frequency of crystal
    f_phonon = QUARTZ_PIEZO["c_shear"] / (2 * thickness_m)
    omega_phonon = 2 * np.pi * f_phonon

    # Crystal geometry
    diameter = 5e-3
    V_crystal = np.pi * (diameter/2)**2 * thickness_m

    # Spin-phonon coupling -> strain
    eta_Hz = eta_cm * 3e10
    eta_rad = 2 * np.pi * eta_Hz

    # Number of Fe ions
    N_fe = fe_ppm * 1e-6 * V_crystal * QUARTZ_PIEZO["rho"] * 6.022e23 / 0.060

    # Zero-point motion
    x_zpf = np.sqrt(HBAR / (2 * QUARTZ_PIEZO["rho"] * V_crystal * omega_phonon))

    # Per-ion coupling to phonon mode
    a_0 = 4.9e-10  # quartz lattice parameter
    g_sp_per_ion = eta_rad * x_zpf / a_0

    # Collective coupling
    g_sp_collective = g_sp_per_ion * np.sqrt(max(N_fe, 1))

    # Magnon linewidth (dilute spins)
    H_earth = 5e-5  # T baseline
    gamma_m = 0.1 * GAMMA * MU_0 * H_earth  # alpha=0.1 for Fe in quartz

    # Phonon linewidth
    gamma_b = omega_phonon / Q_mech

    # Driven phonon displacement from magnon perturbation
    # x = g_collective * delta_omega / (detuning * gamma_b) * x_zpf
    # At resonance (detuning ~ gamma_b): x ~ g_collective * delta_omega / gamma_b^2 * x_zpf
    x_driven = g_sp_collective * delta_omega_magnon / (gamma_b * omega_phonon) * x_zpf

    # Strain from driven displacement
    strain = x_driven / thickness_m

    # Get piezo voltage
    piezo = piezo_voltage_from_strain(strain, thickness_m, Q_mech=Q_mech)

    return {
        "delta_B_T": delta_B_T,
        "delta_f_magnon_Hz": delta_f_magnon,
        "N_fe_ions": N_fe,
        "g_sp_collective_Hz": g_sp_collective / (2 * np.pi),
        "x_driven_m": x_driven,
        "strain": strain,
        "V_piezo_V": piezo["V_resonant_V"],
        "E_field_Vm": piezo["E_field_resonant_Vm"],
        "P_available_W": piezo["P_available_W"],
        "f_resonance_Hz": piezo["f_resonance_Hz"],
    }


