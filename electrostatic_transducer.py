def parallel_plate_force(V, gap_m, area_m2, epsilon_r=1.0):
    """
    Electrostatic force between parallel plates.
    F = epsilon_0 * epsilon_r * A * V^2 / (2 * d^2)

    V       : voltage across gap (V)
    gap_m   : plate separation (m)
    area_m2 : plate overlap area (m^2)
    epsilon_r: dielectric constant of gap material
    returns : dict with force, pressure, energy
    """
    F = EPSILON_0 * epsilon_r * area_m2 * V**2 / (2 * gap_m**2)
    pressure = F / area_m2 if area_m2 > 0 else 0
    E_stored = 0.5 * EPSILON_0 * epsilon_r * area_m2 * V**2 / gap_m

    # Breakdown check
    E_field = V / gap_m
    E_breakdown = SILICON["dielectric_strength"]
    safe = E_field < E_breakdown

    return {
        "force_N": F,
        "pressure_Pa": pressure,
        "E_stored_J": E_stored,
        "E_field_Vm": E_field,
        "below_breakdown": safe,
        "margin_to_breakdown": E_breakdown / E_field if E_field > 0 else np.inf,
    }


def comb_drive_force(V, n_fingers, finger_length_m, gap_m,
                     thickness_m, epsilon_r=1.0):
    """
    MEMS comb drive — the workhorse electrostatic actuator.
    Force is CONSTANT with displacement (unlike parallel plate).

    F = n * epsilon_0 * epsilon_r * t * V^2 / gap

    n_fingers      : number of finger pairs
    finger_length_m: finger overlap length (m)
    gap_m          : gap between fingers (m)
    thickness_m    : device layer thickness (m)
    returns        : dict with force, displacement per volt^2
    """
    F = n_fingers * EPSILON_0 * epsilon_r * thickness_m * V**2 / gap_m

    # Capacitance
    C = 2 * n_fingers * EPSILON_0 * epsilon_r * thickness_m * finger_length_m / gap_m

    # Energy
    E = 0.5 * C * V**2

    # Displacement (for a spring-loaded comb drive)
    # x = F / k, but k depends on suspension design
    # Typical MEMS spring: k ~ 0.1 - 10 N/m
    # We'll compute force per unit spring constant
    force_per_k = F  # displacement = F/k, so x = force_per_k / k

    return {
        "force_N": F,
        "capacitance_F": C,
        "E_stored_J": E,
        "force_per_k_m_N_per_m": force_per_k,
        "n_fingers": n_fingers,
    }


def electrostatic_rotor(V, n_poles, rotor_radius_m, gap_m,
                         rotor_thickness_m, rotor_length_m):
    """
    Electrostatic variable-capacitance motor.
    Torque from changing capacitance as rotor turns.

    tau = 0.5 * n_poles * epsilon_0 * (rotor_length / gap) * V^2

    V               : drive voltage (V)
    n_poles         : number of pole pairs
    rotor_radius_m  : rotor radius (m)
    gap_m           : air gap (m)
    rotor_thickness_m: radial thickness (m)
    rotor_length_m  : axial length (m)
    returns         : dict with torque, power, speed
    """
    # Capacitance variation per pole
    dC_dtheta = EPSILON_0 * rotor_length_m * rotor_thickness_m / gap_m

    # Torque
    torque = 0.5 * n_poles * dC_dtheta * V**2

    # Moment of inertia (thin cylindrical shell)
    rho_si = SILICON["density"]
    m_rotor = rho_si * 2 * np.pi * rotor_radius_m * rotor_thickness_m * rotor_length_m
    I_rotor = m_rotor * rotor_radius_m**2

    # Angular acceleration
    alpha_acc = torque / I_rotor if I_rotor > 0 else 0

    # Steady-state speed estimate (limited by air drag at MEMS scale)
    # Drag torque ~ eta * omega * r^3 * L (viscous)
    # At atmospheric pressure: eta_air ~ 1.8e-5 Pa·s
    eta_air = 1.8e-5
    drag_coeff = eta_air * rotor_radius_m**3 * rotor_length_m
    omega_ss = torque / drag_coeff if drag_coeff > 0 else 0
    rpm_ss = omega_ss * 60 / (2 * np.pi)

    # Mechanical power at steady state
    P_mech = torque * omega_ss

    # Electrical power input
    # Switching loss: P = 0.5 * C_total * V^2 * f_switching
    C_total = n_poles * EPSILON_0 * rotor_length_m * rotor_thickness_m / gap_m
    f_switch = n_poles * omega_ss / (2 * np.pi)
    P_elec = 0.5 * C_total * V**2 * f_switch

    efficiency = P_mech / P_elec if P_elec > 0 else 0

    return {
        "torque_Nm": torque,
        "m_rotor_kg": m_rotor,
        "I_rotor_kgm2": I_rotor,
        "alpha_rad_s2": alpha_acc,
        "omega_steady_rad_s": omega_ss,
        "rpm_steady": rpm_ss,
        "P_mechanical_W": P_mech,
        "P_electrical_W": P_elec,
        "efficiency": min(efficiency, 1.0),
        "C_total_F": C_total,
    }


# ─────────────────────────────────────────────
# MEMS DEVICE CONFIGURATIONS
# ─────────────────────────────────────────────

def config_mems_micro(V_drive=1.0):
    """
    Micro-scale MEMS electrostatic motor.
    Typical silicon MEMS: 100um rotor, 2um gap, 50 poles.
    """
    return electrostatic_rotor(
        V=V_drive,
        n_poles=50,
        rotor_radius_m=50e-6,
        gap_m=2e-6,
        rotor_thickness_m=10e-6,
        rotor_length_m=20e-6,
    )


def config_mems_milli(V_drive=10.0):
    """
    Milli-scale electrostatic motor.
    Larger: 1mm rotor, 10um gap, 100 poles.
    More torque, lower speed.
    """
    return electrostatic_rotor(
        V=V_drive,
        n_poles=100,
        rotor_radius_m=500e-6,
        gap_m=10e-6,
        rotor_thickness_m=50e-6,
        rotor_length_m=200e-6,
    )


def config_macro_disk(V_drive=100.0):
    """
    Macro-scale electrostatic disk motor.
    1cm rotor, 100um gap, 200 poles.
    Requires higher voltage but produces usable torque.
    """
    return electrostatic_rotor(
        V=V_drive,
        n_poles=200,
        rotor_radius_m=5e-3,
        gap_m=100e-6,
        rotor_thickness_m=500e-6,
        rotor_length_m=1e-3,
    )


# ─────────────────────────────────────────────
# FULL SIGNAL CHAIN: GEOMAGNETIC -> MECHANICAL
# ─────────────────────────────────────────────

def full_transduction_chain(
    delta_B_T=500e-9,           # geomagnetic storm ~500 nT
    fe_ppm=100,                 # Fe concentration in quartz
    eta_cm=1.5,                 # spin-phonon coupling (cm^-1)
    quartz_thickness_m=0.1e-3,  # 100 um AT-cut quartz
    Q_mech=1e6,                 # mechanical Q
    motor_config="mems_micro",  # motor size
):
    """
    Complete signal chain from geomagnetic variation to mechanical work.

    1. Geomagnetic dB -> magnon frequency shift (Layer 0b)
    2. Spin-phonon coupling -> lattice vibration (magnonic)
    3. Piezoelectric effect -> voltage (quartz)
    4. Electrostatic force -> rotor torque (silicon MEMS)
    5. Rotation -> mechanical work

    Returns dict with full chain analysis and efficiency at each stage.
    """
    # Stage 1-3: Geomagnetic -> piezo voltage
    piezo = piezo_voltage_from_magnon(
        delta_B_T=delta_B_T,
        fe_ppm=fe_ppm,
        eta_cm=eta_cm,
        thickness_m=quartz_thickness_m,
        Q_mech=Q_mech,
    )

    V_piezo = piezo["V_piezo_V"]

    # Stage 4: Piezo voltage -> electrostatic motor
    if motor_config == "mems_micro":
        motor = config_mems_micro(V_drive=V_piezo)
    elif motor_config == "mems_milli":
        motor = config_mems_milli(V_drive=V_piezo)
    elif motor_config == "macro_disk":
        motor = config_macro_disk(V_drive=V_piezo)
    else:
        motor = config_mems_micro(V_drive=V_piezo)

    # Energy accounting
    E_magnetic = HBAR * GAMMA * MU_0 * delta_B_T  # energy per magnon perturbation
    E_mechanical = motor["P_mechanical_W"] / max(piezo["f_resonance_Hz"], 1)

    # Overall efficiency
    eta_overall = E_mechanical / E_magnetic if E_magnetic > 0 else 0

    # Minimum detectable field change (motor produces measurable rotation)
    # Thermal noise torque: tau_th = sqrt(4 k_B T gamma_drag / omega)
    eta_air = 1.8e-5
    r = 50e-6 if motor_config == "mems_micro" else 500e-6 if motor_config == "mems_milli" else 5e-3
    drag_coeff = eta_air * r**3 * r  # rough
    tau_thermal = np.sqrt(4 * K_B * 300 * drag_coeff)
    # Motor torque must exceed thermal noise
    # tau_motor = 0.5 * n * dC/dtheta * V^2
    # V_min where tau_motor = tau_thermal
    n_poles = motor.get("C_total_F", 1e-15) / max(EPSILON_0, 1e-30)  # rough
    V_min_detect = np.sqrt(2 * tau_thermal / max(motor["torque_Nm"] / max(V_piezo**2, 1e-30), 1e-30))

    # What geomagnetic field change produces V_min_detect?
    # V_piezo scales linearly with delta_B (through the chain)
    sensitivity = V_piezo / delta_B_T if delta_B_T > 0 else 0
    B_min_detect = V_min_detect / sensitivity if sensitivity > 0 else np.inf

    return {
        "input": {
            "delta_B_T": delta_B_T,
            "fe_ppm": fe_ppm,
            "eta_spin_phonon_cm": eta_cm,
            "Q_mech": Q_mech,
            "motor_config": motor_config,
        },
        "stage_1_magnon": {
            "delta_f_magnon_Hz": piezo["delta_f_magnon_Hz"],
            "N_fe_ions": piezo["N_fe_ions"],
        },
        "stage_2_phonon": {
            "x_driven_m": piezo["x_driven_m"],
            "strain": piezo["strain"],
            "f_resonance_Hz": piezo["f_resonance_Hz"],
        },
        "stage_3_piezo": {
            "V_piezo_V": V_piezo,
            "P_available_W": piezo["P_available_W"],
        },
        "stage_4_motor": {
            "torque_Nm": motor["torque_Nm"],
            "rpm_steady": motor["rpm_steady"],
            "P_mechanical_W": motor["P_mechanical_W"],
            "efficiency": motor["efficiency"],
        },
        "overall": {
            "E_magnetic_J": E_magnetic,
            "E_mechanical_J": E_mechanical,
            "eta_overall": eta_overall,
            "V_sensitivity_V_per_T": sensitivity,
            "B_min_detectable_T": B_min_detect,
            "tau_thermal_Nm": tau_thermal,
        },
        "materials": {
            "quartz": "AT-cut SiO2 + Fe3+ defects (piezoelectric source)",
            "silicon": "MEMS rotor (electrostatic actuator)",
            "magnets": "NONE",
            "copper": "NONE (no windings)",
            "rare_earth": "NONE",
        },
    }


# ─────────────────────────────────────────────
# COUPLING STATE EXPORT
# ─────────────────────────────────────────────

def coupling_state(delta_B_T=500e-9, fe_ppm=100, eta_cm=1.5,
                   quartz_thickness_m=0.1e-3, Q_mech=1e6,
                   motor_config="mems_micro"):
    """
    State vector for coupling to other layers.
    Same interface contract as other coupling_state() functions.
    """
    chain = full_transduction_chain(
        delta_B_T=delta_B_T,
        fe_ppm=fe_ppm,
        eta_cm=eta_cm,
        quartz_thickness_m=quartz_thickness_m,
        Q_mech=Q_mech,
        motor_config=motor_config,
    )

    return {
        "V_piezo_V": chain["stage_3_piezo"]["V_piezo_V"],
        "P_available_W": chain["stage_3_piezo"]["P_available_W"],
        "torque_Nm": chain["stage_4_motor"]["torque_Nm"],
        "rpm_steady": chain["stage_4_motor"]["rpm_steady"],
        "P_mechanical_W": chain["stage_4_motor"]["P_mechanical_W"],
        "motor_efficiency": chain["stage_4_motor"]["efficiency"],
        "B_sensitivity_T": chain["overall"]["B_min_detectable_T"],
        "eta_overall": chain["overall"]["eta_overall"],
        "motor_config": motor_config,
    }


